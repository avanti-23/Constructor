package com.tka.dec24;

public class Player {
	
	int jersey_no;
	String player_name;
	int runs;
	int wickets;
	String team_name;
	
	Player(int j_no,String p_name,int r,int w,String t_name){
		
		jersey_no = j_no;
		player_name = p_name;
		runs = r;
		wickets = w;
		team_name = t_name;
		
	}
	

}
